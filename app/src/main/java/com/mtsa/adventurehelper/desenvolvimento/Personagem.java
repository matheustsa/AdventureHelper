public class Personagem {

    private String idPersonagem;
    private String nome;
    private String classe;
    private String raca;
    private String lvl;

    private String FORCA;
    private String DESTREZA;
    private String CONTITUICAO;
    private String INTELIGENCIA;
    private String SABEDORIA;
    private String CARISMA;


    //Getters e Setters
    public String getIdPersonagem() {
        return idPersonagem;
    }

    public void setIdPersonagem(String idPersonagem) {
        this.idPersonagem = idPersonagem;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getClasse() {
        return classe;
    }

    public void setClasse(String classe) {
        this.classe = classe;
    }

    public String getRaca() {
        return raca;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public String getLvl() {
        return lvl;
    }

    public void setLvl(String lvl) {
        this.lvl = lvl;
    }

    public String getFORCA() {
        return FORCA;
    }

    public void setFORCA(String FORCA) {
        this.FORCA = FORCA;
    }

    public String getDESTREZA() {
        return DESTREZA;
    }

    public void setDESTREZA(String DESTREZA) {
        this.DESTREZA = DESTREZA;
    }

    public String getCONTITUICAO() {
        return CONTITUICAO;
    }

    public void setCONTITUICAO(String CONTITUICAO) {
        this.CONTITUICAO = CONTITUICAO;
    }

    public String getINTELIGENCIA() {
        return INTELIGENCIA;
    }

    public void setINTELIGENCIA(String INTELIGENCIA) {
        this.INTELIGENCIA = INTELIGENCIA;
    }

    public String getSABEDORIA() {
        return SABEDORIA;
    }

    public void setSABEDORIA(String SABEDORIA) {
        this.SABEDORIA = SABEDORIA;
    }

    public String getCARISMA() {
        return CARISMA;
    }

    public void setCARISMA(String CARISMA) {
        this.CARISMA = CARISMA;
    }
}
